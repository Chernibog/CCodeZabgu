#include <stdio.h>
#include <windows.h>  // Библиотека для настройки кодировки консоли Windows (UTF-8)
#include <math.h>     // Для использования функции pow() — возведение в степень

int main() {
    // Устанавливаем вывод консоли в кодировке UTF-8 для корректного отображения кириллицы
    SetConsoleOutputCP(CP_UTF8);
    int n;

    printf("Введите размерность матрицы: ");
    scanf("%d",&n);

    // Объявляем двумерный массив размером 8x8, который будет представлять матрицу
    int matrix[n][n];

    // Объявляем одномерный массив из 8 элементов для хранения введённых пользователем чисел
    int x_array[n];

    // Цикл для заполнения массива x_array значениями, введёнными пользователем
    for (int i = 0; i < n; i++) {
        printf("Впишите значение x: ");
        scanf("%d", &x_array[i]);  // Считываем целое число и сохраняем в x_array[i]
    }

    // Цикл для заполнения матрицы по определённому правилу:
    // Каждый элемент матрицы равен x_j в степени (i+1), где i - номер строки, j - номер столбца
    for (int i = 0; i < n; i++) {               // Перебираем строки матрицы
        for (int j = 0; j < n; j++) {           // Перебираем столбцы матрицы
            matrix[i][j] = pow(x_array[j], i + 1);  // Вычисляем x_j в степень (i+1)

            // Выводим текущий элемент матрицы и пробел после него
            printf("%d", matrix[i][j]);
            printf(" ");
        }
        // После окончания строки матрицы переходим на новую строку в выводе
        puts(" ");
    }

    return 0;  // Завершаем программу с кодом 0 (успешное выполнение)
}