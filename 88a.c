#include <stdio.h>   // Библиотека для работы с функциями ввода/вывода (например, printf и scanf)
#include <math.h>    // Библиотека математических функций (pow, abs, tan и т.д.)
#include <windows.h> // Библиотека Windows для настройки кодировки консоли

int main() {

    // Установка кодировки вывода консоли на UTF-8 для корректного отображения русского языка
    SetConsoleOutputCP(CP_UTF8);

    float n;              // Переменная для хранения введённого пользователем числа
    int i;                // Счётчик для цикла
    char string[20];      // Массив символов (строка), в который будет записано число как текст

    // Вывод приглашения для пользователя
    printf("Введите число n: ");

    // Считываем значение из консоли и сохраняем его в переменную n
    // &n — передаём адрес переменной, чтобы scanf мог записать туда значение
    scanf("%f", &n);

    // Вычисляем квадрат числа n
    n = pow(n, 2);

    // Преобразуем число n (уже возведённое в квадрат) в строку и сохраняем в массив string
    sprintf(string, "%f", n);

    // Цикл для проверки каждого символа в строке
    for (i = 0; i != 20; i++) {
        // Проверяем, есть ли в строковом представлении числа цифра '3'
        if (string[i] == '3') {
            // Если нашли цифру '3', выводим сообщение и результат
            printf("В числе есть цифра 3, n^2 = %.1f", n);
            return 1; // Прерываем программу после первого совпадения
        }

    }
    printf("В числе отсуствует цифра 3, n^2 = %.1f", n);

    return 0; // Завершение программы
}